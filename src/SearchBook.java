    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ISBN = jTextField1.getText();
        try
        {
            Connection con = ProvideConnection.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM book WHERE ISBN='"+ISBN+"'");
            if(rs.next())
            {
                jTextField2.setText(rs.getString(2));
                jTextField6.setText(rs.getString(3));
                jTextField3.setText("Available");
                ResultSet rs1 = st.executeQuery("SELECT DISTINCT author_name FROM book b JOIN author e WHERE b.author_id =e.author_id");
               while(rs1.next())
               {
                   jTextField5.setText(rs1.getString(1));
               }
               ResultSet rs2 = st.executeQuery("SELECT DISTINCT publisher_name FROM book b JOIN publisher p WHERE p.publisher_id = b.publisher_id");
               while(rs2.next())
               {
                   jTextField4.setText(rs2.getString(1));
               }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Book not found in the database!");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
                jTextField6.setText("");
                
            }
            
            
    }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

